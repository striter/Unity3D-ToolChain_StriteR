using System.Collections.Generic;

public static class UChar
{
    private static readonly int kDefaultDigit3x5 = 2+0*16+6*256+4*4096+7*65536;
    private static readonly Dictionary<char,int> fDigitBinaries3x5 = new() {
        {' ',0+0*16+0*256+0*4096+0*65536}, {'-',0+0*16+7*256+0*4096+0*65536}, {'+',0+2*16+7*256+2*4096+0*65536},
        {'0',7+5*16+5*256+5*4096+7*65536}, {'1',7+2*16+2*256+3*4096+2*65536}, {'2',7+1*16+7*256+4*4096+7*65536}, {'3',7+4*16+7*256+4*4096+7*65536},
        {'4',4+4*16+7*256+5*4096+5*65536}, {'5',7+4*16+7*256+1*4096+7*65536}, {'6',7+5*16+7*256+1*4096+7*65536}, {'7',4+4*16+4*256+4*4096+7*65536},
        {'8',7+5*16+7*256+5*4096+7*65536}, {'9',7+4*16+7*256+5*4096+7*65536},
        {'?',2+0*16+6*256+4*4096+7*65536}, {'!',2+0*16+2*256+2*4096+2*65536},
    };
    public static int GetDigitBinary3x5(this char c) => fDigitBinaries3x5.TryGetValue(c, out var value) ? value : kDefaultDigit3x5;
}
